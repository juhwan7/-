import math

# 원주율
print(math.pi)

# 최대 공약수
print(math.gcd(10, 8))

# 올림
print(math.ceil(math.pi))

# 내림
print(math.floor(math.pi))

# 제곱근
print(math.sqrt(25))

# 절대값
# 절대값은 0부터 숫자까지에 거리를 생각하면 되며 0에서 음수까지에 거리를 양수로 나타내기 때문에 1.5가 나온다.
print(math.fabs(-1.5))

#삼각함수
x = 45
math.sin(x) # 사인
math.cos(x) # 코사인
math.tan(x) # 탄젠트
